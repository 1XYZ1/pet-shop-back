/**
 * Enum PetTemperament
 * Define los temperamentos básicos de las mascotas
 * Útil para prevenir situaciones de riesgo durante servicios veterinarios o de grooming
 *
 * Valores:
 * - calm: Tranquilo y dócil
 * - nervous: Nervioso, ansioso
 * - aggressive: Agresivo o reactivo
 * - friendly: Amigable y sociable
 * - unknown: Temperamento aún no evaluado
 */
export enum PetTemperament {
    CALM = 'calm',
    NERVOUS = 'nervous',
    AGGRESSIVE = 'aggressive',
    FRIENDLY = 'friendly',
    UNKNOWN = 'unknown'
}
